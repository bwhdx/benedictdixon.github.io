// Concatenate the compiled .js files into a single bundle.js so the browser
// only fetches one app script instead of five. Order matters — later files
// reference globals defined earlier (window.Runner, window.MorphStage, etc.).

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');

const ORDER = [
  'portrait-renderers.js',
  'cursor-fx.js',
  'morph-stage.js',
  'site-content.js',
  'portfolio-app.js',
];

const banner = `/* Bundle of ${ORDER.join(', ')} — generated by tools/bundle.cjs */\n`;

const out = banner + ORDER
  .map(f => {
    const src = fs.readFileSync(path.join(ROOT, f), 'utf8');
    return `\n/* ── ${f} ────────────────────────────────────────── */\n${src}`;
  })
  .join('\n');

fs.writeFileSync(path.join(ROOT, 'bundle.js'), out);
console.log(`Wrote bundle.js (${(out.length / 1024).toFixed(1)} KB, ${ORDER.length} files)`);
